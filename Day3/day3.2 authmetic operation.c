#include<stdio.h>
int main()
{
	int a=16,b=3;
	int add,sub,multi,div,mod;
	add=a+b;
	sub=a-b;
	multi=a*b;
	div=a/b;
	mod=a%b;
	printf("additional : %d \n",add);
	printf("subtraction : %d \n",sub);
	printf("multiplication : %d \n",multi);
	printf("division : %d \n",div);
	printf("modulus : %d \n",mod);
}
